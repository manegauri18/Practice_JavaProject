package Variables;
//Swap two num using third variable
public class demo21 
{
    public static void main(String[] args) 
    {
		int a=10;
		int b=20;
		int c;
		
		System.out.println("Before swap: ");
		System.out.println("First num is: "+ a);
		System.out.println("second num is: "+ b);
		
		
		c=a;
		a=b;
		b=c;
		
		System.out.println("After swap: ");
		System.out.println("First num is: "+ a);
		System.out.println("second num is: "+ b);
    	
    	
    	
    	
    	
	}
}
