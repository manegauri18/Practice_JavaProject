package Variables;

public class demo20 
{
  //program that takes a number as input 8 and prints its multiplication table upto 10.
	
	public static void main(String[] args) 
	{
	
	     int num=8;
		
		for(int i=1; i<=10; i++)
		{
			
			
			System.out.println(num+"x"+(i)+"="+(num*i));
		}
	}
}
