package control_statements;

public class Sample6_switch2 
{
	public static void main(String[] args) 
	{
	
		String inp="BI";
		
		switch(inp)
		{
		case "CD": System.out.println("Running MS code");
		break;
		
		case "CW": System.out.println("Running MS code");
		break;
		
		case "BI": System.out.println("Running MS code");
		break;
		
		case "MS": System.out.println("Running MS code");
		break;
		
		default:System.out.println("Wrong input");
		
		
		
		
		}
		
	}

 	
	
	
	
}
